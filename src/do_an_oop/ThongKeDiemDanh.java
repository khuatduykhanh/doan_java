    public void getDDbyYear(String y){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<diemdanh> list = DiemDanhDao.getDDbyYear(y);
        dtm.setRowCount(0);
        list.forEach((a) -> {
            dtm.addRow(new Object[]{a.getIdHV(),a.getHoTen(),a.getGoiTap(),a.getNgayTap()});
        });
    }
    
