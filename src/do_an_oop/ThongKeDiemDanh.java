    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = (String) jComboBox1.getSelectedItem();
        if (name.equals("Năm")) {
            JOptionPane.showMessageDialog(null, "Oke");
            getDDbyYear(txtNam_1.getText());
            
        }
        if (name.equals("Tháng")) {
            JOptionPane.showMessageDialog(null, "Oke");
            getDDbyMonth(txtNam_1.getText(),txtThang_1.getText());
        }
        if (name.equals("Ngày")) {
            String date =txtNam_1.getText()+"-"+txtThang_1.getText()+"-"+txtNgay_1.getText();
            JOptionPane.showMessageDialog(null, "Oke");
            getDDbyDay(date);
        }
        if(name.equals("Khoảng thời gian")){
            String date1 =txtNam_1.getText()+"-"+txtThang_1.getText()+"-"+txtNgay_1.getText();
            String date2 =txtNam_2.getText()+"-"+txtThang_2.getText()+"-"+txtNgay_2.getText();
            JOptionPane.showMessageDialog(null, "Oke");
            getDDbyBetweenDay(date1,date2);
        }
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        int i = dtm.getRowCount();
        jLabel2.setText(String.valueOf(i));     
    }//GEN-LAST:event_jButton1ActionPerformed

