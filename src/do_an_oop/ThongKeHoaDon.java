    public void getHDbyYear(String y){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<hoa_don> list = HoaDonDao.getHDbyYear(y);
        dtm.setRowCount(0);
        list.forEach((a) -> {
            dtm.addRow(new Object[]{a.getId(),a.getHoten(),a.getSdt(),a.getEmail(),
                                    a.getGiatien(),a.getGoitap(),a.getNgaydangky()});
        });
    }
    
