    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String name = (String) jComboBox1.getSelectedItem();
        if (name.equals("Năm")) {
            JOptionPane.showMessageDialog(null, "Oke");
            getHDbyYear(txtNam_1.getText());
        }
        if (name.equals("Tháng")) {
            JOptionPane.showMessageDialog(null, "Oke");
            getHDbyMonth(txtNam_1.getText(),txtThang_1.getText());
        }
        if (name.equals("Ngày")) {
            String date =txtNam_1.getText()+"-"+txtThang_1.getText()+"-"+txtNgay_1.getText();
            JOptionPane.showMessageDialog(null, "Oke");
            getHDbyDay(date);
        }
        if(name.equals("Khoảng thời gian")){
            String date1 =txtNam_1.getText()+"-"+txtThang_1.getText()+"-"+txtNgay_1.getText();
            String date2 =txtNam_2.getText()+"-"+txtThang_2.getText()+"-"+txtNgay_2.getText();
            JOptionPane.showMessageDialog(null, "Oke");
            getHDbyBetweenDay(date1,date2);
        }
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        for(int i = 0; i < jTable1.getRowCount();i++){
            tien = Integer.parseInt(dtm.getValueAt(i, 4).toString());
            Sum = Sum +tien;
        }
        jLabel3.setText(String.valueOf(Sum)+" VND");
    }//GEN-LAST:event_jButton2ActionPerformed

